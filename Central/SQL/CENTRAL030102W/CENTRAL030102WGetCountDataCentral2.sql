 SELECT DISTINCT ROW_NUMBER() OVER (ORDER BY a.IDNO ASC) AS ROW_NUM,d.PART_CD,c.SUMM_STS,e.SYSTEM_VALUE_TXT, a.* FROM TB_H_SFBASE_CENTRAL a inner join TB_H_SFN_CENTRAL b on a.IDNO = b.IDNO

  INNER JOIN TB_H_PARTID_H c on b.IDNO =c.IDNO inner join  TB_M_PART d on b.MSG_NO = d.MSG_NO
  inner join TB_M_SYSTEM e on c.SUMM_STS = e.SYSTEM_CD